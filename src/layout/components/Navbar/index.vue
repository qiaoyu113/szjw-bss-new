<script lang="ts">
import { Component, Vue } from 'vue-property-decorator'
import { AppModule } from '@/store/modules/app'
import { UserModule } from '@/store/modules/user'
import Breadcrumb from '@/components/Breadcrumb/index.vue'
import ErrorLog from '@/components/ErrorLog/index.vue'
import Hamburger from '@/components/Hamburger/index.vue'
import HeaderSearch from '@/components/HeaderSearch/index.vue'
import LangSelect from '@/components/LangSelect/index.vue'
import Screenfull from '@/components/Screenfull/index.vue'
import DownTool from '@/components/DownTool/index.vue'
import Setting from '@/components/Setting/index.vue'
import OpenNew from '@/components/OpenNew/index.vue'
import SizeSelect from '@/components/SizeSelect/index.vue'
import { SettingsModule } from '@/store/modules/settings'

@Component({
  name: 'Navbar',
  components: {
    Breadcrumb,
    ErrorLog,
    Hamburger,
    HeaderSearch,
    LangSelect,
    Screenfull,
    DownTool,
    Setting,
    OpenNew,
    SizeSelect
  }
})
export default class extends Vue {
  get userName() {
    return UserModule.name
  }
  // 判断是否是PC
  get isPC() {
    return SettingsModule.isPC
  }

  get sidebar() {
    return AppModule.sidebar
  }

  get device() {
    return AppModule.device.toString()
  }

  get avatar() {
    return UserModule.avatar
  }

  private toggleSideBar() {
    AppModule.ToggleSideBar(false)
  }

  private async logout(this:any) {
    await UserModule.LogOut()
    this.$notify.closeAll()
    let makePhoneDom = document.getElementById('makePhone')
    if ((makePhoneDom as HTMLElement).hasChildNodes()) {
      this.handleDeleteDom()
    }
    this.$router.push(`/login?redirect=${this.$route.fullPath}`)
  }
  // 删除dom
  handleDeleteDom() {
    let makePhone = document.getElementById('makePhone')
    const child = (makePhone as HTMLHtmlElement).children[0];
    (makePhone as HTMLHtmlElement).removeChild(child)
  }
}
</script>

